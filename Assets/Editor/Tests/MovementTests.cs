using UnityEngine;
using UnityEngine.TestTools;
using NUnit.Framework;

public class MovementTests
{
    [Test]
    public void movesAlongXAxisForHorizontalInput()
    {
        Vector3 pos = new Vector3(0f, 0f, 0f);
        Vector3 r = new Vector3(1f, 1f, 2f);
        Vector3 f = new Vector3(0f, 0f, 0f);

        Assert.AreEqual(1, new Movement(1).calculate(pos, 1, 1, r, f).x);
    }

    [Test]
    public void movesAlongZAxisForVerticalInput()
    {
        Vector3 pos = new Vector3(0f, 0f, 0f);
        Vector3 r = new Vector3(0f, 0f, 0f);
        Vector3 f = new Vector3(0f, 0f, 2f);

        Assert.AreEqual(2, new Movement(1).calculate(pos, 1, 1, r, f).z);
    }

    [Test]
    public void movesAlongXAndZAxis()
    {
        Vector3 pos = new Vector3(0.5f, 0f, 0f);
        Vector3 r = new Vector3(1f, 0f, 0f);
        Vector3 f = new Vector3(0.5f, 0f, 2f);

        Vector3 expected = new Vector3(3f, 1f, 2f);

        Assert.AreEqual(expected, new Movement(1).calculate(pos, 2, 1, r, f));
    }
}